function formatData(params) {
  let data = [
    {
      name: "价格",
      data: [
        [
          "藏品名字",
          "最低价格",
          "总发行量",
          "所属系列",
          "作者名字",
          "认证",
          "地址",
        ],
      ],
    },
  ];

  params.forEach((item) => {
    data[0].data.push([
      item.name,
      Number(item.priceCny),
      item.total,
      item.albumName,
      item.author,
      item.authentication,
      item.link,
    ]);
  });

  return data;
}















